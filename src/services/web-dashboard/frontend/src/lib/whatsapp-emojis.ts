const RECENT_KEY = 'radarchat-wa-emoji-recent'
const RECENT_MAX = 24

export function loadRecentEmojis(): string[] {
  try {
    const raw = localStorage.getItem(RECENT_KEY)
    if (!raw) return []
    const parsed = JSON.parse(raw) as unknown
    return Array.isArray(parsed) ? parsed.filter(e => typeof e === 'string').slice(0, RECENT_MAX) : []
  } catch {
    return []
  }
}

export function pushRecentEmoji(emoji: string): string[] {
  const prev = loadRecentEmojis().filter(e => e !== emoji)
  const next = [emoji, ...prev].slice(0, RECENT_MAX)
  try {
    localStorage.setItem(RECENT_KEY, JSON.stringify(next))
  } catch {
    /* quota */
  }
  return next
}
